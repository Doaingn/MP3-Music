    private void MusicArtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MusicArtMouseClicked
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Select Music Art");  // ตั้งชื่อ dialog
    fileChooser.setFileFilter(new FileNameExtensionFilter("Image files", "jpg", "png"));  // กรองเฉพาะไฟล์ภาพ

    // ถ้าผู้ใช้เลือกไฟล์และคลิก Open
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();  // รับไฟล์ที่ผู้ใช้เลือก

            try {
                // นำไฟล์ที่เลือกมาตั้งค่าให้กับ Music
                music.setMusicAlbumArt(selectedFile.getAbsolutePath());

                // ตั้งค่า icon ของ MusicArt ให้เป็นไฟล์ภาพที่เลือก
                ImageIcon icon = new ImageIcon(selectedFile.getAbsolutePath());
                MusicArt.setIcon(icon);  // เปลี่ยน icon ของ MusicArt
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error setting music art: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_MusicArtMouseClicked


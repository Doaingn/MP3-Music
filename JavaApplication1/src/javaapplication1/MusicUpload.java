    private void createMusicFolderIfNotExists() {
        File folder = new File(musicFolderPath);
        if (!folder.exists()) {
            folder.mkdirs(); // สร้างโฟลเดอร์หากยังไม่มี
            System.out.println("Music folder created at: " + musicFolderPath);
        }
    }

    private void initMusic(String filePath) throws UnsupportedTagException, InvalidDataException, IOException {
        try {
               music = new Music(filePath);
               System.out.println("Music file loaded successfully: " + filePath);
           } catch (UnsupportedTagException | InvalidDataException | IOException e) {
               e.printStackTrace();
               JOptionPane.showMessageDialog(this, "Error loading MP3 file: " + e.getMessage());
               music = null; // Reset music object if failed
           }
    }
// เมื่อกดปุ่ม Save
private void saveMusicMetadata() {
    try {
        // รับค่าจาก TextField
        String title = MusicTileTF.getText().trim();
        String artist = MusicArtistTF.getText().trim();
        String album = MusicAlbumTF.getText().trim();
        String genre = (String) GenreCB.getSelectedItem();

        if (title.isEmpty() || artist.isEmpty() || album.isEmpty() || genre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields before saving.");
            return;
        }

        // ตรวจสอบว่า music ถูกสร้างเรียบร้อยหรือยัง
        if (music != null) {
            // ตั้งค่า Metadata
            music.setMusicTitle(title);
            music.setMusicArtist(artist);
            music.setMusicAlbum(album);
            music.setMusicGenre(genre);

            // กำหนด Path สำหรับบันทึกไฟล์
            String outputFilePath = musicFolderPath + "\\" + title.replaceAll("[^a-zA-Z0-9]", "_") + ".mp3";

            // บันทึกข้อมูลลง MP3
            music.save(outputFilePath);

            JOptionPane.showMessageDialog(this, "Music metadata saved successfully at: " + outputFilePath);
        } else {
            JOptionPane.showMessageDialog(this, "No music file initialized. Please upload an MP3 file first.");
        }
    } catch (IOException | NotSupportedException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error saving music metadata: " + e.getMessage());
    }
}


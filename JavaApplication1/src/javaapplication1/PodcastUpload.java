    private void createPodcastFolderIfNotExists() {
        File folder = new File(PodcastFolderPath);
        if (!folder.exists()) {
            folder.mkdirs(); // สร้างโฟลเดอร์หากยังไม่มี
            System.out.println("Podcast folder created at: " + PodcastFolderPath);
        }
    }

    private void initPodcast(String filePath) throws UnsupportedTagException, InvalidDataException {
        try {
            podcast = new Podcast(filePath);
        } catch (IOException | UnsupportedTagException | InvalidDataException e) {
            e.printStackTrace();
        }
    }
// เมื่อกดปุ่ม Save
private void savePodcastMetadata() {
    try {
        // รับค่าจาก TextField
        String title = PodcastTitleTF1.getText();
        String storyteller = StoryTeller.getText();

        // ตรวจสอบว่า podcast ถูกสร้างเรียบร้อยหรือยัง
        if (podcast != null) {
            // ตั้งค่า Metadata
            podcast.setPodcastTitle(title);       // ตั้งค่าหัวข้อของ Podcast
            podcast.setStoryteller(storyteller);  // ตั้งค่าผู้เล่า

            // กำหนด Path สำหรับบันทึกไฟล์
            String outputFilePath = PodcastFolderPath + "\\" + title.replaceAll("[^a-zA-Z0-9]", "_") + ".mp3";

            // บันทึกข้อมูลลง MP3
            podcast.save(outputFilePath);

        } else {
            System.out.println("No podcast file initialized!");
        }
    } catch (IOException | NotSupportedException e) {
        e.printStackTrace();
    }
}

    
    

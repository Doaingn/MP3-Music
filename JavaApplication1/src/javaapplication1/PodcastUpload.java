    private void ChooseMp3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ChooseMp3MouseClicked
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileFilter(new FileNameExtensionFilter("MP3 Files", "mp3"));

        // ถ้าผู้ใช้เลือกไฟล์
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            // โหลดไฟล์ MP3
            try {
                initPodcast(selectedFile.getAbsolutePath());  // โหลดไฟล์ MP3 ให้กับ podcast
                MP3Filename.setText(selectedFile.getName());  // แสดงชื่อไฟล์ใน UI
            } catch (UnsupportedTagException | InvalidDataException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error loading MP3 file: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_ChooseMp3MouseClicked

    private void PodcastArtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PodcastArtMouseClicked
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Select Podcast Art");  // ตั้งชื่อ dialog
    fileChooser.setFileFilter(new FileNameExtensionFilter("Image files", "jpg", "png"));  // กรองเฉพาะไฟล์ภาพ

    // ถ้าผู้ใช้เลือกไฟล์และคลิก Open
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();  // รับไฟล์ที่ผู้ใช้เลือก

            try {
                // นำไฟล์ที่เลือกมาตั้งค่าให้กับ Podcast
                podcast.setPodcastArt(selectedFile.getAbsolutePath());

                // ตั้งค่า icon ของ PodcastArt ให้เป็นไฟล์ภาพที่เลือก
                ImageIcon icon = new ImageIcon(selectedFile.getAbsolutePath());
                PodcastArt.setIcon(icon);  // เปลี่ยน icon ของ PodcastArt
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error setting podcast art: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_PodcastArtMouseClicked

